first = int(input("첫 번째 정수 : "))
secound = int(input("두 번째 정수: "))

print("{} + {} = {}".format(first, secound, (first + secound)))
print("{} - {} = {}".format(first, secound, (first - secound)))
print("{} * {} = {}".format(first, secound, (first * secound)))
print("{} / {} = {}".format(first, secound, (first / secound)))
print("{} % {} = {}".format(first, secound, (first % secound)))
print("{} ** {} = {}".format(first, secound, (first ** secound)))


